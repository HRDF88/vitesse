package com.example.vitesse.data.webservice

/**
 * Contains constant values used for configuring currency-related API requests.
 */
object CurrencyConstants {

    /**
     * The base currency for currency exchange calculations.
     * This constant represents the currency from which the exchange rates are calculated.
     * Example: "EUR" (Euro)
     */
    const val BASE_CURRENCY = "EUR"

    /**
     * The foreign currency for currency exchange calculations.
     * This constant represents the target currency to which the base currency is converted.
     * Example: "GBP" (British Pound Sterling)
     */
    const val FOREIGN_CURRENCY = "GBP"
}